export function validateDbTableObjectName(value: string): string | null {
  const name = value.trim();
  if (name.length === 0) {
    return 'db-table plugin is missing pluginConfig.table.';
  }
  if (/^(SELECT|WITH)\b/i.test(name)) {
    return 'db-table pluginConfig.table contains SQL. Set pluginConfig.table to a table/view name and put SELECT/WITH SQL in plugin.txt.';
  }
  if (/\s/.test(name)) {
    return 'db-table pluginConfig.table contains spaces. Use a table/view name without spaces, and put SELECT/WITH SQL in plugin.txt.';
  }
  if (!/^[A-Za-z_][A-Za-z0-9_]*$/.test(name)) {
    return 'db-table pluginConfig.table must be a simple SQLite identifier using letters, numbers, and underscores, starting with a letter or underscore.';
  }
  if (SQLITE_RESERVED_WORDS.has(name.toUpperCase())) {
    return `db-table pluginConfig.table uses reserved SQLite word "${name}". Choose a non-keyword table/view name.`;
  }
  return null;
}
